function renderContent(lang) {
  const t = translations[lang] || translations.en;
  let html = `<h1>${t.title}</h1>`;

  // Show both effective date and last update if available
  if (t.effectiveDate) {
    html += `<p class="update-date">${t.effectiveDate}</p>`;
  }
  html += `<p class="update-date">${t.lastUpdate}</p><hr>`;

  t.sections.forEach(section => {
    html += `<h2>${section.title}</h2><p>${section.content}</p>`;
    if (section.list) {
      html += '<ul>' + section.list.map(item => `<li>${item}</li>`).join('') + '</ul>';
    }
  });

  html += `<hr><p><em>${t.footer}</em></p>`;
  document.getElementById('content').innerHTML = html;
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    renderContent(btn.dataset.lang);
    document.documentElement.lang = btn.dataset.lang;
  });
});

// Load default or browser language
const browserLang = navigator.language.split('-')[0];
const defaultLang = translations[browserLang] ? browserLang : 'en';
document.querySelector(`[data-lang="${defaultLang}"]`)?.classList.add('active');
renderContent(defaultLang);
